# built in list method copy()
list1=["dog","bird","horse"]
newlist=list1.copy()
print(newlist)
print("\n")

# make a copy is to use the built-in method list()
list2=["apple","banana","mango"]
mylist=list(list2)
print(mylist)
print("\n")

# slice operator
list3=["lily","rose","sunflower"]
list4=list3[:]
print(list4)
print("\n")
